# print all the elements present in a set
a = [1, 2, 3, 4, 5]
for i in a:
    print(i)

# using range in for loop
for i in range(6, 11):
    print(i)

# using for loop for printing values in the tuple and using the else command in for loop
b = (11, 12, 13, 14, 15)
for i in b:
    print(i)
else:
    print('Printing Completed!')
